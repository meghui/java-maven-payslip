public class CSV {

    private String firstName = null;
    private String lastName = null;
    private String annualSalary = null;
    private String superRate = null;
    private String startDateOfPayment = null;

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getAnnualSalary() {
        return annualSalary;
    }

    public void setAnnualSalary(String annualSalary) {
        this.annualSalary = annualSalary;
    }

    public String getSuperRate() {
        return superRate;
    }

    public void setSuperRate(String superRate) {
        this.superRate = superRate;
    }

    public String getStartDateOfPayment() {
        return startDateOfPayment;
    }

    public void setStartDateOfPayment(String startDateOfPayment) {
        this.startDateOfPayment = startDateOfPayment;
    }
}
